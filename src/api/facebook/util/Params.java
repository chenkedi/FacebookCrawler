package api.facebook.util;

public class Params
{
	private boolean useProxy=true;
	private String proxyIp;
	private int proxyPort;
	
	private int seedsInfoLength;
	private int postsInfoLength;
	private int feedsInfoLength;
	private int commentsLength;
	public boolean isUseProxy() {
		return useProxy;
	}
	public void setUseProxy(boolean useProxy) {
		this.useProxy = useProxy;
	}
	public String getProxyIp() {
		return proxyIp;
	}
	public void setProxyIp(String proxyIp) {
		this.proxyIp = proxyIp;
	}
	public int getProxyPort() {
		return proxyPort;
	}
	public void setProxyPort(int proxyPort) {
		this.proxyPort = proxyPort;
	}
	public int getSeedsInfoLength() {
		return seedsInfoLength;
	}
	public void setSeedsInfoLength(int seedsInfoLength) {
		this.seedsInfoLength = seedsInfoLength;
	}
	public int getPostsInfoLength() {
		return postsInfoLength;
	}
	public void setPostsInfoLength(int postsInfoLength) {
		this.postsInfoLength = postsInfoLength;
	}
	
	public int getFeedsInfoLength() {
		return feedsInfoLength;
	}
	public void setFeedsInfoLength(int feedsInfoLength) {
		this.feedsInfoLength = feedsInfoLength;
	}
	public int getCommentsLength() {
		return commentsLength;
	}
	public void setCommentsLength(int commentsLength) {
		this.commentsLength = commentsLength;
	}
	
	
}
